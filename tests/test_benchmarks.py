class TestSampleEfficiency:
    """Tests for sample efficiency metrics."""

    def test_efficiency_metric_computed(self):
        """Sample efficiency should be computable."""
        initial_error = 0.5
        final_error = 0.05
        total_samples = 100

        error_reduction = initial_error - final_error
        efficiency = error_reduction / total_samples

        assert efficiency > 0
        assert efficiency == pytest.approx(0.0045)

    def test_efficiency_comparison_uniform_vs_active(self):
        """Compare efficiency of different sampling strategies."""
        # Mock results
        uniform_result = {
            "initial_error": 0.5,
            "final_error": 0.1,
            "samples": 200,
        }
        active_result = {
            "initial_error": 0.5,
            "final_error": 0.1,
            "samples": 100,
        }

        uniform_efficiency = (uniform_result["initial_error"] - uniform_result["final_error"]) / uniform_result["samples"]
        active_efficiency = (active_result["initial_error"] - active_result["final_error"]) / active_result["samples"]

        # Active learning should be more efficient
        assert active_efficiency > uniform_efficiency


